            $metrics['inside'] = ($data['inside'] != null) ? round($data['inside'], 2) : null;
            $metrics['outside'] = ($data['outside'] != null) ? round($data['outside'], 2) : null;

            if ($metrics['inside'] == null) {
                $metrics['alpha'] = null;
            }

            $rb->setMetrics($metrics);
            $this->command->save($rb);

            $pointer++;
